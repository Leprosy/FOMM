    public void checkboxUpdate() {
        this.jCheckBox1.setSelected((this.flag[this.X][this.Y] & _IMPASSABLE) == _IMPASSABLE);
        this.jCheckBox2.setSelected((this.flag[this.X][this.Y] & _DOOR) == _DOOR);
        this.jCheckBox3.setSelected((this.flag[this.X][this.Y] & _GRATE) == _GRATE);
        this.jCheckBox4.setSelected((this.flag[this.X][this.Y] & _SECRET) == _SECRET);
        this.jCheckBox5.setSelected((this.flag[this.X][this.Y] & _WATER) == _WATER);
        this.jCheckBox6.setSelected((this.flag[this.X][this.Y] & _DEEP) == _DEEP);
    }
    
    public void tableFocus(int x, int y) {
        this.X = x;
        this.Y = y;
        this.jTextField1.setText("x : " + x + "  -  y : " + y + " > " + this.flag[x][y]);

        //this.jTable1.setCellSelectionEnabled(true);
        this.jTable1.changeSelection(x, y, false, false);
        this.jTable1.requestFocus();

        //this.jTable2.setCellSelectionEnabled(true);
        this.jTable2.changeSelection(x, y, false, false);
        this.jTable2.requestFocus();

        //this.jTable3.setCellSelectionEnabled(true);
        this.jTable3.changeSelection(x, y, false, false);
        this.jTable3.requestFocus();
    }
    
    public void updateFlags() {
        this.flag[this.X][this.Y] = (byte)(   
                (this.jCheckBox1.isSelected() ? 1 : 0) * _IMPASSABLE |
                (this.jCheckBox2.isSelected() ? 1 : 0) * _DOOR |
                (this.jCheckBox3.isSelected() ? 1 : 0) * _GRATE |
                (this.jCheckBox4.isSelected() ? 1 : 0) * _SECRET |
                (this.jCheckBox5.isSelected() ? 1 : 0) * _WATER |
                (this.jCheckBox6.isSelected() ? 1 : 0) * _DEEP
            );
    }

