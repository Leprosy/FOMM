    private void saveAsMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveAsMenuItemActionPerformed
        //Save the fucking map
        try {
            this.EdConsole.append("Saving map...\n\n");
            
            //Filechoose dialog
            JFileChooser fc = new JFileChooser();
            fc.showDialog(jPanel1, "Save this crap!");
            DataOutputStream os = new DataOutputStream(new FileOutputStream(fc.getSelectedFile().toString()));

            //Map meta width, height, start coordinates
            os.writeByte((byte)this.M_WIDTH);
            os.writeByte((byte)this.M_HEIGHT);
            os.writeByte((byte)1);
            os.writeByte((byte)1); /** @todo: fix this! user must _select starting point*/

            for (int i = 0; i < this.M_WIDTH; ++i) {
                for (int j = 0; j < this.M_HEIGHT; ++j) {
                    String bt = this.jTable1.getValueAt(i,j).toString();
                    os.writeByte(bt.charAt(0));
                }
            }
            for (int i = 0; i < this.M_WIDTH; ++i) {
                for (int j = 0; j < this.M_HEIGHT; ++j) {
                    String bt = this.jTable2.getValueAt(i,j).toString();
                    os.writeByte(bt.charAt(0));
                }
            }
            for (int i = 0; i < this.M_WIDTH; ++i) {
                for (int j = 0; j < this.M_HEIGHT; ++j) {
                    String bt = this.jTable3.getValueAt(i,j).toString();
                    os.writeByte(bt.charAt(0));
                }
            }
            for (int i = 0; i < this.M_WIDTH; ++i) {
                for (int j = 0; j < this.M_HEIGHT; ++j) {
                    os.writeByte(this.flag[i][j]);
                }
            }

            this.EdConsole.append("Saving complete...\n\n");

            // Close
            os.close();
        } catch (Exception e) {
            this.EdConsole.append("ERROR cannot save map:( .\n\n");
            e.printStackTrace();
        }
    }//GEN-LAST:event_saveAsMenuItemActionPerformed

